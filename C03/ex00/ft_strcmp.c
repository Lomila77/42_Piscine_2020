int	ft_strcmp(char *s1, char *s2)
{
	int i;

	i = -1;
	if (!*s1 || !*s2)
		return (*s1 - *s2);
	while (s1[++i] && s2[i])
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
	return (0);
}
