char	*ft_strcat(char *dest, char *src)
{
	int i;
	int a;

	i = -1;
	a = -1;
	while (dest[++i])
		;
	while (src[++a])
	{
		dest[i] = src[a];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
