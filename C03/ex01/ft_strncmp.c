int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	i = -1;
	if (!*s1 || !*s2)
		return (*s1 - *s2);
	while (s1[++i] || s2[i] && i < n)
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
	return (0);
}
