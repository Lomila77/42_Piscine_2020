#include <stdlib.h>

int	ft_ultimate_range(int **range, int min, int max)
{
	int i;

	i = max - min;
	*range = NULL;
	if (min >= max)
		return (0);
	if ((*range = malloc(sizeof(int) * i)) == NULL)
		return (-1);
	i = 0;
	while (min < max)
	{
		(*range)[i] = min;
		min++;
		i++;
	}
	return (i);
}
